package com.db.entity;

import java.util.List;

public class MenuTree {
	private Integer menuId;
	private String menuName;
	private String menuUrl;
	private int parentId;
	private int menuLevel;
	private String menuLogo;
	private Integer orderId;
	private Integer hasAuthority;//临时
	private List<MenuTree> childs;
	
	public Integer getMenuId() {
		return menuId;
	}
	public void setMenuId(Integer menuId) {
		this.menuId = menuId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuUrl() {
		return menuUrl;
	}
	public void setMenuUrl(String menuUrl) {
		this.menuUrl = menuUrl;
	}
	public int getParentId() {
		return parentId;
	}
	public void setParentId(int parentId) {
		this.parentId = parentId;
	}
	public int getMenuLevel() {
		return menuLevel;
	}
	public void setMenuLevel(int menuLevel) {
		this.menuLevel = menuLevel;
	}
	public String getMenuLogo() {
		return menuLogo;
	}
	public void setMenuLogo(String menuLogo) {
		this.menuLogo = menuLogo;
	}
	public Integer getOrderId() {
		return orderId;
	}
	public void setOrderId(Integer orderId) {
		this.orderId = orderId;
	}
	public List<MenuTree> getChilds() {
		return childs;
	}
	public void setChilds(List<MenuTree> childs) {
		this.childs = childs;
	}
	public Integer getHasAuthority() {
		return hasAuthority;
	}
	public void setHasAuthority(Integer hasAuthority) {
		this.hasAuthority = hasAuthority;
	}
}