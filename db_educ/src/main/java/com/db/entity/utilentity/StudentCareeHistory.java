package com.db.entity.utilentity;

import java.util.List;

public class StudentCareeHistory {
	private Integer studentId;
	private String headImage;
	private String realName;
	private String userName;
	private List<SubjectPaper> subPaperList;

	public Integer getStudentId() {
		return studentId;
	}

	public void setStudentId(Integer studentId) {
		this.studentId = studentId;
	}

	public String getHeadImage() {
		return headImage;
	}

	public void setHeadImage(String headImage) {
		this.headImage = headImage;
	}

	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public List<SubjectPaper> getSubPaperList() {
		return subPaperList;
	}

	public void setSubPaperList(List<SubjectPaper> subPaperList) {
		this.subPaperList = subPaperList;
	}

	@Override
	public String toString() {
		return "StudentCareeHistory [studentId=" + studentId + ", headImage=" + headImage + ", realName=" + realName
				+ ", userName=" + userName + ", subPaperList=" + subPaperList + "]";
	}
}
